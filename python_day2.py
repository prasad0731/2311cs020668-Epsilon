# List
my_list = [10, 20, 30, 40, 50]
first_element = my_list[0]
second_element = my_list[1]
last_element = my_list[-1]
print(f"First element: {first_element}")
print(f"Second element: {second_element}")
print(f"Last element: {last_element}")

# Tuple
my_tuple = (100, 200, 300, 400, 500)
first_element = my_tuple[0]
third_element = my_tuple[2]
fourth_element = my_tuple[3]
print(f"First element: {first_element}")
print(f"Third element: {third_element}")
print(f"Fourth element: {fourth_element}")

# Dictionary
my_dict = {
    'key1': 'value1',
    'key2': 'value2',
    'key3': 'value3',
    'key4': 'value4',
    'key5': 'value5'
}
value_of_key1 = my_dict['key1']
value_of_key3 = my_dict['key3']
value_of_key5 = my_dict['key5']
print(f"Value of key1: {value_of_key1}")
print(f"Value of key3: {value_of_key3}")
print(f"Value of key5: {value_of_key5}")
